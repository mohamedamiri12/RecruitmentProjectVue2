<template>
  <div class="c-app">
    <TheSidebar/>
    <CWrapper>
      <TheHeader/>
      <div class="c-body">
        <main class="c-main">
          <CContainer fluid>
            <transition name="fade" mode="out-in">
              <router-view :key="$route.path" :administrator="administrator"></router-view>
            </transition>
          </CContainer>
        </main>
      </div>
      <TheFooter/>
    </CWrapper>
  </div>
</template>

<script>
import TheSidebar from './TheSidebar'
import TheHeader from './TheHeader'
import TheFooter from './TheFooter'

export default {
  name: 'TheContainer',
  components: {
    TheSidebar,
    TheHeader,
    TheFooter
  },
  computed:{
    administrator(){
      return this.$store.getters.getActifAdministrator;
    }
  },
  created(){
    const activeAdmin = JSON.parse(localStorage.getItem('admin'))
    const token = localStorage.getItem('token')
    if(activeAdmin && token ){
       this.$store.commit('SET_ACTIF_ADMINISTRATOR',activeAdmin);
       this.$store.commit('SET_TOKEN_ACCESS',token)
    }
  }
}
</script>

<style scoped>
.fade-enter-active,
.fade-leave-active {
  transition: opacity 0.3s;
}
.fade-enter,
.fade-leave-to {
  opacity: 0;
}
</style>
